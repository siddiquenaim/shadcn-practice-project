// Function to add unique ID to JSON object
function addUniqueId(jsonData: MenuItem[]) {
  let index = 0;
  for (let key in jsonData) {
    jsonData[key].id = (index++).toString() + "item";
    jsonData[key].title = jsonData[key].meta;
  }
  return jsonData;
}

